'''Program to print a number in words'''
a=int(input("Enter a number :"))
if(a==0):
    print("Zero")
if (a==1):
    print("One")
elif(a==2):
    print("Two")
elif(a==3):
    print("Three")
elif(a==4):
    print("Four")
elif(a==5):
    print("Five")
elif(a==6):
    print("Six")
elif(a==7):
    print("Seven")
elif(a==8):
    print("Eight")
elif(a==9):
    print("Nine")
else:
    print("Not a single digit number")
